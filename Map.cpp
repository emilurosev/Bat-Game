Map::Map()
{
    dirt = TextureManager::loadTexture("assets/dirt.png");
    grass = TextureManager::loadTexture("assets/grass.png");
    water = TextureManager::loadTexture("assets/water.png");

    loadMapLVL1(lvl1);

    src.x = src.y = 0;
    src.w = dest.w = tileWidth;
    src.h = dest.h = tileHeight;

    dest.x = dest.y = 0;

}

void Map::loadMapLVL1(int arr[20][25]) {
    for(int i = 0; i < 20; i++) {
        for(int j = 0; j < 25; j++) {
            mapa[i][j] = arr[i][j];
        }
    }
}




void Map::drawMap() {
    int type = 0;

    for(int i = 0; i < 20; i++) {
        dest.y = i * tileHeight;
        for(int j = 0; j < 25; j++) {
            dest.x = j * tileWidth;
            type = mapa[i][j];
            switch(type) {
                case 0:
                    SDL_RenderCopy(Game::renderer, water, &src, &dest);
                    break;
                case 1:
                    SDL_RenderCopy(Game::renderer, grass, &src, &dest);
                    break;
                case 2:
                    SDL_RenderCopy(Game::renderer, dirt, &src, &dest);
                    break;
            }
        }
    }
}
